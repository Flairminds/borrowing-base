export const generateAssetData = (assetFormData) => {
  let assetdata = {};
  for (let i = 0; i < assetFormData.length; i++) {
    const key = assetFormData[i].key;
    const value = assetFormData[i].value;
    assetdata = {...assetdata, [key]: value};
  }
  return assetdata;
};

